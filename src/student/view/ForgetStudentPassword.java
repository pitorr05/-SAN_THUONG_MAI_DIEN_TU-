    public boolean isValidForm() {
        return !txtStudentID.getText().isEmpty()
                && !txtGmail.getText().isEmpty()
                && !txtDate.getText().isEmpty()
                && !txtNewPassword.getText().isEmpty();
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String studentID = txtStudentID.getText();
        String gmail = txtGmail.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date ngaySinhDate = null;
        try {
            ngaySinhDate = sdf.parse(txtDate.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "vui lòng nhập đúng định dạng ngày sinh (vd: 22/1/2002)");
            return;
        }
        String ngaySinhString = sdf.format(ngaySinhDate);
        String newPassword = txtNewPassword.getText();        
        if ( !isValidForm() ) {
            JOptionPane.showMessageDialog(this, "Bạn vui lòng điền đầy đủ thông tin");
        } else {
            DataStudentFunctionImp dsfi = new DataStudentFunctionImp();
            Student s = dsfi.getStudentByID(studentID);
            String studentDateString = sdf.format(s.getDob());
            if (s == null) {
                JOptionPane.showMessageDialog(this, "Mã sinh viên của bạn không tồn tại.");
                return;
            } else {
                if( gmail.equals(s.getEmail()) && studentDateString.equals(ngaySinhString)) {
                    s.setPassword(newPassword);
                    dsfi.updateStudent(s);
                } else {
                    JOptionPane.showMessageDialog(this, "Bạn đã nhập sai thông tin.");
                    return;
                }
            }
            this.dispose();
            JOptionPane.showMessageDialog(this, "Bạn đã đổi mật khẩu thành công");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

