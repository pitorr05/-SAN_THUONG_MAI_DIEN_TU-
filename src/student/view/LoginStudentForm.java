        st = new Student(studentId, passwd);
        st = checkStudentId(st);
        if(st != null){
            InForStudentRegister inforStRg = new InForStudentRegister(st);
            inforStRg.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Bạn đã nhập sai thông tin.");
        }
